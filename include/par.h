#ifndef __PAR_H
#define __PAR_H

// Includes
#include <PA9.h>

template <class T, class V>
struct par {
	//Miembros
	T first;
	V second;

	//Constructores
	par () {}
	par (const T& obj1, const V& obj2) {
		first = obj1;
		second = obj2;
	}
	par (const par& orig) {
		first=orig.first;
		second = orig.second;
	}
};

#endif