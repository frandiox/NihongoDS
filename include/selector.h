#ifndef SELECTOR_H_
#define SELECTOR_H_

#include <PA9.h>
#include "all_gfx.h"

inline void* selector(u8 i) {
	switch (i) {
	case 0:
		return (void*) hiragana_a_Sprite;
	case 1:
		return (void*) hiragana_e_Sprite;
	case 2:
		return (void*) hiragana_i_Sprite;
	case 3:
		return (void*) hiragana_o_Sprite;
	case 4:
		return (void*) hiragana_u_Sprite;
	case 5:
		return (void*) hiragana_doble_a_Sprite;
	case 6:
		return (void*) hiragana_doble_o_Sprite;
	case 7:
		return (void*) hiragana_doble_u_Sprite;
	case 8:
		return (void*) kanji_1_Sprite;
	case 9:
		return (void*) kanji_2_Sprite;
	case 10:
		return (void*) kanji_3_Sprite;
	case 11:
		return (void*) kanji_4_Sprite;
	default:
		return NULL;
	}
}


#endif /* SELECTOR_H_ */
